export function genIsosurface(
  n: number, l: number, m: number, Zeff: number, densityLevel: number, probabilityPercent: number
): IsosurfaceData {
  const gridN = [16, 24, 32, 40, 48][Math.min(densityLevel, 4)] || 32;
  const rMax = (n * n * 2.5 + n * 4) * a0 / Zeff;
  const bnd = rMax * 1.2;
  const threshold = (probabilityPercent / 100) ** 2;

  const step = (2 * bnd) / gridN;
  const sz = gridN + 1;

  function evalPsi2(x: number, y: number, z: number): number {
    return psi2Cart(n, l, m, x, y, z, Zeff);
  }

  // Build field on grid
  const F = new Float32Array(sz * sz * sz);
  const idx = (x: number, y: number, z: number) => x + y * sz + z * sz * sz;
  for (let z = 0; z < sz; z++) {
    for (let y = 0; y < sz; y++) {
      for (let x = 0; x < sz; x++) {
        F[idx(x, y, z)] = evalPsi2(-bnd + x * step, -bnd + y * step, -bnd + z * step);
      }
    }
  }

  // 3D box blur to reduce artifacts
  const B = new Float32Array(sz * sz * sz);
  for (let z = 1; z < sz - 1; z++) {
    for (let y = 1; y < sz - 1; y++) {
      for (let x = 1; x < sz - 1; x++) {
        const c = F[idx(x, y, z)];
        const s6 = F[idx(x - 1, y, z)] + F[idx(x + 1, y, z)] +
                   F[idx(x, y - 1, z)] + F[idx(x, y + 1, z)] +
                   F[idx(x, y, z - 1)] + F[idx(x, y, z + 1)];
        B[idx(x, y, z)] = c * 0.5 + s6 * (0.5 / 6);
      }
    }
  }
  // Boundary copy
  for (let z = 0; z < sz; z++) {
    for (let y = 0; y < sz; y++) {
      B[idx(0, y, z)] = F[idx(0, y, z)];
      B[idx(sz - 1, y, z)] = F[idx(sz - 1, y, z)];
    }
  }
  for (let z = 0; z < sz; z++) {
    for (let x = 0; x < sz; x++) {
      B[idx(x, 0, z)] = F[idx(x, 0, z)];
      B[idx(x, sz - 1, z)] = F[idx(x, sz - 1, z)];
    }
  }
  for (let y = 0; y < sz; y++) {
    for (let x = 0; x < sz; x++) {
      B[idx(x, y, 0)] = F[idx(x, y, 0)];
      B[idx(x, y, sz - 1)] = F[idx(x, y, sz - 1)];
    }
  }

  // Marching cubes
  const verts: number[] = [];

  function lerp(p1: number[], p2: number[], v1: number, v2: number): number[] {
    if (Math.abs(threshold - v1) < 1e-12) return p1.slice();
    if (Math.abs(threshold - v2) < 1e-12) return p2.slice();
    if (Math.abs(v1 - v2) < 1e-12) return p1.slice();
    const mu = (threshold - v1) / (v2 - v1);
    return [p1[0] + mu * (p2[0] - p1[0]), p1[1] + mu * (p2[1] - p1[1]), p1[2] + mu * (p2[2] - p1[2])];
  }

  for (let z = 0; z < gridN; z++) {
    for (let y = 0; y < gridN; y++) {
      for (let x = 0; x < gridN; x++) {
        const px = -bnd + x * step, py = -bnd + y * step, pz = -bnd + z * step;
        const s = step;
        const corners = [
          [px, py, pz + s], [px + s, py, pz + s], [px + s, py, pz], [px, py, pz],
          [px, py + s, pz + s], [px + s, py + s, pz + s], [px + s, py + s, pz], [px, py + s, pz],
        ];
        const vals = [
          B[idx(x, y, z + 1)], B[idx(x + 1, y, z + 1)], B[idx(x + 1, y, z)], B[idx(x, y, z)],
          B[idx(x, y + 1, z + 1)], B[idx(x + 1, y + 1, z + 1)], B[idx(x + 1, y + 1, z)], B[idx(x, y + 1, z)],
        ];

        let cubeIndex = 0;
        for (let i = 0; i < 8; i++) if (vals[i] < threshold) cubeIndex |= 1 << i;

        const edges = MC_EDGE_TABLE[cubeIndex];
        if (edges === 0) continue;

        const edgeVerts: (number[] | null)[] = new Array(12).fill(null);
        if (edges & 0x001) edgeVerts[0] = lerp(corners[0], corners[1], vals[0], vals[1]);
        if (edges & 0x002) edgeVerts[1] = lerp(corners[1], corners[2], vals[1], vals[2]);
        if (edges & 0x004) edgeVerts[2] = lerp(corners[2], corners[3], vals[2], vals[3]);
        if (edges & 0x008) edgeVerts[3] = lerp(corners[3], corners[0], vals[3], vals[0]);
        if (edges & 0x010) edgeVerts[4] = lerp(corners[4], corners[5], vals[4], vals[5]);
        if (edges & 0x020) edgeVerts[5] = lerp(corners[5], corners[6], vals[5], vals[6]);
        if (edges & 0x040) edgeVerts[6] = lerp(corners[6], corners[7], vals[6], vals[7]);
        if (edges & 0x080) edgeVerts[7] = lerp(corners[7], corners[4], vals[7], vals[4]);
        if (edges & 0x100) edgeVerts[8] = lerp(corners[0], corners[4], vals[0], vals[4]);
        if (edges & 0x200) edgeVerts[9] = lerp(corners[1], corners[5], vals[1], vals[5]);
        if (edges & 0x400) edgeVerts[10] = lerp(corners[2], corners[6], vals[2], vals[6]);
        if (edges & 0x800) edgeVerts[11] = lerp(corners[3], corners[7], vals[3], vals[7]);

        const triList = MC_TRI_TABLE[cubeIndex] as number[];
        for (let i = 0; i < triList.length; i += 3) {
          const a = edgeVerts[triList[i]];
          const b = edgeVerts[triList[i + 1]];
          const c = edgeVerts[triList[i + 2]];
          if (a && b && c) {
            verts.push(...a, ...b, ...c);
          }
        }
      }
    }
  }

  if (verts.length === 0) {
    return { vertices: new Float32Array(0), normals: new Float32Array(0) };
  }

  const vertices = new Float32Array(verts);
  const normals = new Float32Array(vertices.length);

  // Compute normals as cross products of edge vectors
  for (let i = 0; i < vertices.length; i += 9) {
    const a = [vertices[i], vertices[i + 1], vertices[i + 2]];
    const b = [vertices[i + 3], vertices[i + 4], vertices[i + 5]];
    const c = [vertices[i + 6], vertices[i + 7], vertices[i + 8]];

    const ab = [b[0] - a[0], b[1] - a[1], b[2] - a[2]];
    const ac = [c[0] - a[0], c[1] - a[1], c[2] - a[2]];
    let nx = ab[1] * ac[2] - ab[2] * ac[1];
    let ny = ab[2] * ac[0] - ab[0] * ac[2];
    let nz = ab[0] * ac[1] - ab[1] * ac[0];
    const len = Math.sqrt(nx * nx + ny * ny + nz * nz) || 1;
    nx /= len; ny /= len; nz /= len;

    for (let j = 0; j < 3; j++) {
      normals[i + j * 3] = nx;
      normals[i + j * 3 + 1] = ny;
      normals[i + j * 3 + 2] = nz;
    }
  }

  return { vertices, normals };
}
